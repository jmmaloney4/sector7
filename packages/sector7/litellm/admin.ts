/**
 * Resource options accepted by sector7 dynamic resources.
 *
 * Pulumi dynamic resources are executed by the Node.js dynamic provider runtime,
 * not by a cloud provider plugin. Passing `provider` or `providers` makes Pulumi
 * route the resource through the wrong provider bridge, which fails with a
 * misleading `pulumi-nodejs:dynamic:Resource` unknown-token error.
 */
export type DynamicResourceOptions = Omit<
	CustomResourceOptions,
	"provider" | "providers"
>;

// ---------------------------------------------------------------------------
// Resolved provider input/state shapes
//
// Pulumi resolves every `Input<T>` before invoking the dynamic provider, so the
// callbacks below see plain JSON values only. Optional fields are normalized to
// sentinel empty values ("" / [] / {}) by the wrapping component so the provider
// never has to reason about `undefined`.
// ---------------------------------------------------------------------------

/** Coordinates + credentials for reaching the LiteLLM admin API. */
interface AdminTarget {
	proxyNamespace: string;
	masterKey: string;
	proxyDeploymentName: string;
	proxyPort: number;
}

interface TeamProviderInputs extends AdminTarget {
	teamAlias: string;
	/** Caller-requested team_id; "" lets LiteLLM assign one. */
	desiredTeamId: string;
	models: string[];
	/** "" when unset. */
	maxBudget: number | "";
	budgetDuration: string;
	tags: string[];
	metadata: Record<string, string>;
}

interface TeamProviderState extends TeamProviderInputs {
	/** The team_id actually in LiteLLM (resolved on create/adopt). */
	teamId: string;
}

interface KeyProviderInputs extends AdminTarget {
	keyAlias: string;
	/** The `sk-...` value, generated by a RandomPassword outside the provider. */
	keyValue: string;
	models: string[];
	teamId: string;
	userId: string;
	budgetId: string;
	maxBudget: number | "";
	budgetDuration: string;
	duration: string;
	aliases: Record<string, string>;
	tags: string[];
	metadata: Record<string, string>;
}

interface KeyProviderState extends KeyProviderInputs {
	/** The LiteLLM token hash returned by /key/generate. */
	tokenId: string;
}

