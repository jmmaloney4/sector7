function buildTeamBody(
	inputs: TeamProviderInputs,
	teamIdOverride?: string,
): Record<string, unknown> {
	const body: Record<string, unknown> = {
		team_alias: inputs.teamAlias,
		models: inputs.models,
		metadata: inputs.metadata,
	};
	// tags is a LiteLLM Enterprise feature — only send it when non-empty.
	if (inputs.tags.length > 0) body.tags = inputs.tags;
	const teamId = teamIdOverride || inputs.desiredTeamId;
	if (teamId) body.team_id = teamId;
	if (inputs.maxBudget !== "") body.max_budget = inputs.maxBudget;
	if (inputs.budgetDuration) body.budget_duration = inputs.budgetDuration;
	return body;
}

/** Team /team/update body that also clears fields removed since the last apply. */
function buildTeamUpdateBody(
	olds: TeamProviderState,
	news: TeamProviderInputs,
	teamId: string,
): Record<string, unknown> {
	const body = buildTeamBody(news, teamId);
	applyClearedFields(body, olds, news);
	return body;
}

const teamProvider: dynamic.ResourceProvider = {
	async check(
		_olds: TeamProviderState,
		news: TeamProviderInputs,
	): Promise<dynamic.CheckResult> {
		const failures: dynamic.CheckFailure[] = [];
		for (const property of [
			"proxyNamespace",
			"masterKey",
			"proxyDeploymentName",
			"teamAlias",
		] as const) {
			if (!news[property]) {
				failures.push({ property, reason: `${property} is required` });
			}
		}
		return { inputs: news, failures };
	},

	async diff(
		_id: string,
		olds: TeamProviderState,
		news: TeamProviderInputs,
	): Promise<dynamic.DiffResult> {
		const replaces: string[] = [];
		// A different explicit team_id is a different team — replace. (Empty desired
		// id means "let LiteLLM keep the assigned one"; not a replacement trigger.)
		if (news.desiredTeamId && olds.desiredTeamId !== news.desiredTeamId) {
			replaces.push("desiredTeamId");
		}
		const changed =
			replaces.length > 0 ||
			adminTargetChanged(olds, news) ||
			stableJson(olds.models) !== stableJson(news.models) ||
			olds.teamAlias !== news.teamAlias ||
			(olds.maxBudget ?? "") !== (news.maxBudget ?? "") ||
			(olds.budgetDuration ?? "") !== (news.budgetDuration ?? "") ||
			stableJson(olds.tags ?? []) !== stableJson(news.tags ?? []) ||
			stableJson(olds.metadata ?? {}) !== stableJson(news.metadata ?? {});
		return { changes: changed, replaces, deleteBeforeReplace: false };
	},

	async create(inputs: TeamProviderInputs): Promise<dynamic.CreateResult> {
		return withProxyBaseUrl(inputs, async (baseUrl) => {
			const existing = await findTeamId(
				baseUrl,
				inputs.masterKey,
				inputs.desiredTeamId,
			);
			let teamId = existing;
			if (teamId) {
				// Adopt a pre-existing team (idempotent create) and reconcile its
				// settings to the desired state in the same step.
				await adminRequest(
					baseUrl,
					inputs.masterKey,
					"/team/update",
					"POST",
					buildTeamBody(inputs, teamId),
				);
			} else {
				const resp = await adminRequest(
					baseUrl,
					inputs.masterKey,
					"/team/new",
					"POST",
					buildTeamBody(inputs),
				);
				teamId = resp?.team_id ?? inputs.desiredTeamId;
			}
			if (!teamId) {
				throw new Error("LiteLLM /team/new returned no team_id");
			}
			return { id: teamId, outs: { ...inputs, teamId } };
		});
	},

	async update(
		id: string,
		olds: TeamProviderState,
		news: TeamProviderInputs,
	): Promise<dynamic.UpdateResult> {
		return withProxyBaseUrl(news, async (baseUrl) => {
			await adminRequest(
				baseUrl,
				news.masterKey,
				"/team/update",
				"POST",
				buildTeamUpdateBody(olds, news, id),
			);
			return { outs: { ...news, teamId: id } };
		});
	},

	async delete(id: string, props: TeamProviderState): Promise<void> {
		if (!id) return;
		await withProxyBaseUrl(props, async (baseUrl) => {
			await adminRequest(baseUrl, props.masterKey, "/team/delete", "POST", {
				team_ids: [id],
			});
		});
	},
};

