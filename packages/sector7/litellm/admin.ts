/**
 * Open a short-lived port-forward to a ready pod of the LiteLLM proxy
 * deployment, invoke `fn` with a `http://127.0.0.1:<port>` base URL, then tear
 * the forward down. This mirrors the reachability model of the previous
 * `kubectl exec` approach (works wherever kube credentials work, no tailnet
 * dependency) while running entirely in-process.
 */
async function withProxyBaseUrl<T>(
	target: AdminTarget,
	fn: (baseUrl: string) => Promise<T>,
): Promise<T> {
	const k8s = await import("@kubernetes/client-node");
	const net = await import("node:net");

	const kc = new k8s.KubeConfig();
	kc.loadFromDefault();

	const apps = kc.makeApiClient(k8s.AppsV1Api);
	const core = kc.makeApiClient(k8s.CoreV1Api);

	// Resolve the deployment's pod selector, then pick a ready pod. Reading the
	// selector at runtime avoids hardcoding the proxy's label scheme and works
	// for any deployment name the caller passes.
	const depResp = await apps.readNamespacedDeployment({
		name: target.proxyDeploymentName,
		namespace: target.proxyNamespace,
	});
	// client-node 1.x returns the body directly; tolerate the 0.x {body} shape.
	// biome-ignore lint/suspicious/noExplicitAny: k8s client response is loosely typed across majors
	const dep: any = (depResp as any)?.body ?? depResp;
	const matchLabels: Record<string, string> =
		dep?.spec?.selector?.matchLabels ?? {};
	const labelSelector = Object.entries(matchLabels)
		.map(([k, v]) => `${k}=${v}`)
		.join(",");
	if (!labelSelector) {
		throw new Error(
			`deployment ${target.proxyNamespace}/${target.proxyDeploymentName} has no spec.selector.matchLabels`,
		);
	}

	const podResp = await core.listNamespacedPod({
		namespace: target.proxyNamespace,
		labelSelector,
	});
	// biome-ignore lint/suspicious/noExplicitAny: k8s client response is loosely typed across majors
	const pods: any[] = ((podResp as any)?.body ?? podResp)?.items ?? [];
	const ready =
		pods.find(
			// biome-ignore lint/suspicious/noExplicitAny: pod object is loosely typed
			(p: any) =>
				p?.status?.phase === "Running" &&
				(p?.status?.conditions ?? []).some(
					// biome-ignore lint/suspicious/noExplicitAny: condition object is loosely typed
					(c: any) => c?.type === "Ready" && c?.status === "True",
				),
		) ?? pods[0];
	const podName: string | undefined = ready?.metadata?.name;
	if (!podName) {
		throw new Error(
			`no running pod found for deployment ${target.proxyNamespace}/${target.proxyDeploymentName}`,
		);
	}

	const forward = new k8s.PortForward(kc);
	const server = net.createServer((socket) => {
		forward
			.portForward(
				target.proxyNamespace,
				podName,
				[target.proxyPort],
				socket,
				null,
				socket,
			)
			.catch((err: unknown) =>
				socket.destroy(err instanceof Error ? err : new Error(String(err))),
			);
	});

	await new Promise<void>((resolve, reject) => {
		server.once("error", reject);
		server.listen(0, "127.0.0.1", () => resolve());
	});

	const address = server.address();
	const localPort =
		address && typeof address === "object" ? address.port : undefined;
	if (!localPort) {
		await new Promise<void>((resolve) => server.close(() => resolve()));
		throw new Error("failed to bind a local port for the LiteLLM port-forward");
	}

	try {
		return await fn(`http://127.0.0.1:${localPort}`);
	} finally {
		await new Promise<void>((resolve) => server.close(() => resolve()));
	}
}

/** Issue an authenticated request against the LiteLLM admin API. */
async function adminRequest(
	baseUrl: string,
	masterKey: string,
	path: string,
	method: "GET" | "POST",
	body?: unknown,
	// biome-ignore lint/suspicious/noExplicitAny: admin API responses are dynamic JSON
): Promise<any> {
	const response = await fetch(`${baseUrl}${path}`, {
		method,
		headers: {
			Authorization: `Bearer ${masterKey}`,
			...(body !== undefined ? { "Content-Type": "application/json" } : {}),
		},
		body: body !== undefined ? JSON.stringify(body) : undefined,
	});

	const text = await response.text();
	if (!response.ok) {
		throw new Error(
			`LiteLLM ${method} ${path} failed: ${response.status} ${response.statusText}\n${text}`,
		);
	}
	if (!text) return {};

	// biome-ignore lint/suspicious/noExplicitAny: admin API responses are dynamic JSON
	let parsed: any;
	try {
		parsed = JSON.parse(text);
	} catch {
		return text;
	}
	if (parsed && typeof parsed === "object" && parsed.error) {
		throw new Error(
			`LiteLLM ${method} ${path} returned an error: ${JSON.stringify(parsed.error)}`,
		);
	}
	return parsed;
}

/**
 * Find an existing team to adopt, matched by explicit `team_id` only.
 *
 * Adoption is deliberately NOT done by `team_alias`: aliases are not unique on a
 * LiteLLM admin plane, so matching one would let a new resource adopt and
 * overwrite an unrelated team's models/budgets/metadata — an authorization
 * boundary problem on a shared control plane. A team created without an explicit
 * `team_id` has no stable identity to re-find, so it is always created fresh
 * (callers that need idempotent adoption must pass a stable `team_id`).
 */
async function findTeamId(
	baseUrl: string,
	masterKey: string,
	desiredTeamId: string,
): Promise<string | undefined> {
	if (!desiredTeamId) return undefined;
	const data = await adminRequest(baseUrl, masterKey, "/team/list", "GET");
	// biome-ignore lint/suspicious/noExplicitAny: team list shape varies by LiteLLM version
	const teams: any[] = Array.isArray(data)
		? data
		: (data?.teams ?? data?.data ?? []);
	for (const team of teams) {
		if (!team || typeof team !== "object") continue;
		if (team.team_id === desiredTeamId) return team.team_id;
	}
	return undefined;
}

/**
 * Find an existing key by alias, returning its token hash.
 *
 * LiteLLM `/key/info` never exposes the `sk-...` value, only the hash — so this
 * is used solely to detect a pre-existing key during adoption/recreation, not
 * to recover the secret.
 *
 * The match is scoped to BOTH `alias` and `teamId`: aliases are not globally
 * unique in LiteLLM, so matching on alias alone would let `create()` delete a
 * same-named key belonging to a different team (or a manually-managed key) on a
 * shared admin plane. Requiring the team to match confines deletion to keys
 * this resource actually owns.
 */
async function findKeyHashByAlias(
	baseUrl: string,
	masterKey: string,
	alias: string,
	teamId: string,
): Promise<string | undefined> {
	const data = await adminRequest(baseUrl, masterKey, "/key/list", "GET");
	// biome-ignore lint/suspicious/noExplicitAny: key list shape varies by LiteLLM version
	const rawKeys: any[] = Array.isArray(data)
		? data
		: (data?.keys ?? data?.data ?? []);
	const hashes: string[] = rawKeys
		// biome-ignore lint/suspicious/noExplicitAny: key entry may be a string or object
		.map((k: any) => (typeof k === "string" ? k : (k?.token ?? k?.key_name)))
		.filter((h: unknown): h is string => typeof h === "string" && h.length > 0);
	if (hashes.length === 0) return undefined;

	const infos = await Promise.all(
		hashes.map(async (hash) => {
			try {
				const info = await adminRequest(
					baseUrl,
					masterKey,
					`/key/info?key=${encodeURIComponent(hash)}`,
					"GET",
				);
				return { hash, info: info?.info ?? info };
			} catch {
				return { hash, info: undefined };
			}
		}),
	);
	for (const { hash, info } of infos) {
		if (
			info &&
			typeof info === "object" &&
			info.key_alias === alias &&
			(info.team_id ?? "") === (teamId ?? "")
		) {
			return hash;
		}
	}
	return undefined;
}

/**
 * True when any admin-connection field changed. Such a change does not alter
 * the remote team/key, but it must still flow into stored state so a later
 * update/delete targets the new deployment and uses the new master key rather
 * than the stale ones — so diff() treats it as an in-place change.
 */
function adminTargetChanged(olds: AdminTarget, news: AdminTarget): boolean {
	return (
		olds.proxyNamespace !== news.proxyNamespace ||
		olds.proxyDeploymentName !== news.proxyDeploymentName ||
		olds.proxyPort !== news.proxyPort ||
		olds.masterKey !== news.masterKey
	);
}

/** Stable JSON for order-insensitive comparison in diff(). */
function stableJson(value: unknown): string {
	if (Array.isArray(value)) {
		return JSON.stringify([...value].map(stableValue).sort());
	}
	return JSON.stringify(stableValue(value));
}

function stableValue(value: unknown): unknown {
	if (Array.isArray(value)) return value.map(stableValue);
	if (value && typeof value === "object") {
		const obj = value as Record<string, unknown>;
		return Object.keys(obj)
			.sort()
			.reduce<Record<string, unknown>>((acc, key) => {
				acc[key] = stableValue(obj[key]);
				return acc;
			}, {});
	}
	return value;
}

/**
 * LiteLLM's `/team/update` and `/key/update` treat an *omitted* field as "leave
 * unchanged", so the create/generate bodies that simply skip empty optionals
 * cannot clear a value that was previously set. When a field transitions from
 * set (olds) to empty (news), send an explicit reset (`null` / `[]`) so the
 * change actually propagates instead of silently persisting remotely while
 * Pulumi records the empty state. Shared by the team and key update paths.
 */
function applyClearedFields(
	body: Record<string, unknown>,
	olds: { maxBudget?: number | ""; budgetDuration?: string; tags?: string[] },
	news: { maxBudget: number | ""; budgetDuration: string; tags: string[] },
): void {
	if (
		olds.maxBudget !== "" &&
		olds.maxBudget !== undefined &&
		news.maxBudget === ""
	) {
		body.max_budget = null;
	}
	if (olds.budgetDuration && !news.budgetDuration) body.budget_duration = null;
	if ((olds.tags?.length ?? 0) > 0 && news.tags.length === 0) body.tags = [];
}

