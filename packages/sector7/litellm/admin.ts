function buildKeyGenerateBody(
	inputs: KeyProviderInputs,
): Record<string, unknown> {
	const body: Record<string, unknown> = {
		key_alias: inputs.keyAlias,
		key: inputs.keyValue,
		models: inputs.models,
		aliases: inputs.aliases,
		metadata: inputs.metadata,
	};
	if (inputs.tags.length > 0) body.tags = inputs.tags;
	if (inputs.teamId) body.team_id = inputs.teamId;
	if (inputs.userId) body.user_id = inputs.userId;
	if (inputs.budgetId) body.budget_id = inputs.budgetId;
	if (inputs.maxBudget !== "") body.max_budget = inputs.maxBudget;
	if (inputs.budgetDuration) body.budget_duration = inputs.budgetDuration;
	if (inputs.duration) body.duration = inputs.duration;
	return body;
}

function buildKeyUpdateBody(
	olds: KeyProviderState,
	news: KeyProviderInputs,
): Record<string, unknown> {
	// /key/update is keyed on the token value (LiteLLM hashes it server-side).
	// team_id / key value changes are handled by replacement, not update.
	const body: Record<string, unknown> = {
		key: news.keyValue,
		key_alias: news.keyAlias,
		models: news.models,
		aliases: news.aliases,
		metadata: news.metadata,
	};
	if (news.tags.length > 0) body.tags = news.tags;
	if (news.userId) body.user_id = news.userId;
	if (news.budgetId) body.budget_id = news.budgetId;
	if (news.maxBudget !== "") body.max_budget = news.maxBudget;
	if (news.budgetDuration) body.budget_duration = news.budgetDuration;
	if (news.duration) body.duration = news.duration;
	// Clear fields removed since the last apply (omission would leave them set).
	applyClearedFields(body, olds, news);
	if (olds.duration && !news.duration) body.duration = null;
	return body;
}

const keyProvider: dynamic.ResourceProvider = {
	async check(
		_olds: KeyProviderState,
		news: KeyProviderInputs,
	): Promise<dynamic.CheckResult> {
		const failures: dynamic.CheckFailure[] = [];
		for (const property of [
			"proxyNamespace",
			"masterKey",
			"proxyDeploymentName",
			"keyAlias",
			"keyValue",
		] as const) {
			if (!news[property]) {
				failures.push({ property, reason: `${property} is required` });
			}
		}
		return { inputs: news, failures };
	},

	async diff(
		_id: string,
		olds: KeyProviderState,
		news: KeyProviderInputs,
	): Promise<dynamic.DiffResult> {
		const replaces: string[] = [];
		// Rotating the secret or moving teams means a new key in LiteLLM.
		if (olds.keyValue !== news.keyValue) replaces.push("keyValue");
		if ((olds.teamId ?? "") !== (news.teamId ?? "")) replaces.push("teamId");
		const changed =
			replaces.length > 0 ||
			adminTargetChanged(olds, news) ||
			(olds.keyAlias ?? "") !== (news.keyAlias ?? "") ||
			stableJson(olds.models) !== stableJson(news.models) ||
			stableJson(olds.aliases ?? {}) !== stableJson(news.aliases ?? {}) ||
			stableJson(olds.metadata ?? {}) !== stableJson(news.metadata ?? {}) ||
			stableJson(olds.tags ?? []) !== stableJson(news.tags ?? []) ||
			(olds.userId ?? "") !== (news.userId ?? "") ||
			(olds.budgetId ?? "") !== (news.budgetId ?? "") ||
			(olds.maxBudget ?? "") !== (news.maxBudget ?? "") ||
			(olds.budgetDuration ?? "") !== (news.budgetDuration ?? "") ||
			(olds.duration ?? "") !== (news.duration ?? "");
		return { changes: changed, replaces, deleteBeforeReplace: true };
	},

	async create(inputs: KeyProviderInputs): Promise<dynamic.CreateResult> {
		return withProxyBaseUrl(inputs, async (baseUrl) => {
			// Idempotency: LiteLLM /key/info can't return the original sk-... value,
			// so a pre-existing key with this alias can't be adopted in place. Delete
			// it and recreate with our known (RandomPassword-derived) key value.
			const existingHash = await findKeyHashByAlias(
				baseUrl,
				inputs.masterKey,
				inputs.keyAlias,
				inputs.teamId,
			);
			if (existingHash) {
				await adminRequest(baseUrl, inputs.masterKey, "/key/delete", "POST", {
					keys: [existingHash],
				});
			}
			const resp = await adminRequest(
				baseUrl,
				inputs.masterKey,
				"/key/generate",
				"POST",
				buildKeyGenerateBody(inputs),
			);
			const tokenId: string = resp?.token ?? resp?.token_id ?? "";
			// Never fall back to the sk-... value: Pulumi resource IDs are stored
			// in plaintext in state, so using the secret as the ID would leak the
			// credential (and delete expects a LiteLLM token hash, not the secret).
			if (!tokenId) {
				throw new Error("LiteLLM /key/generate returned no token id");
			}
			return {
				id: tokenId,
				outs: { ...inputs, tokenId },
			};
		});
	},

	async update(
		id: string,
		olds: KeyProviderState,
		news: KeyProviderInputs,
	): Promise<dynamic.UpdateResult> {
		return withProxyBaseUrl(news, async (baseUrl) => {
			await adminRequest(
				baseUrl,
				news.masterKey,
				"/key/update",
				"POST",
				buildKeyUpdateBody(olds, news),
			);
			return { outs: { ...news, tokenId: olds.tokenId || id } };
		});
	},

	async delete(id: string, props: KeyProviderState): Promise<void> {
		const token = id || props.tokenId;
		if (!token) return;
		await withProxyBaseUrl(props, async (baseUrl) => {
			await adminRequest(baseUrl, props.masterKey, "/key/delete", "POST", {
				keys: [token],
			});
		});
	},
};

